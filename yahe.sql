SET NAMES UTF8;
DROP DATABASE IF EXISTS yahe;
CREATE DATABASE yahe CHARSET=UTF8;
USE yahe;

/**用户信息**/
CREATE TABLE fuser(
  uid INT PRIMARY KEY AUTO_INCREMENT,
  uname VARCHAR(32),
  upwd VARCHAR(32),
  email VARCHAR(64),
  phone VARCHAR(16),
  avatar VARCHAR(128) DEFAULT 'img/header/user.png',        #头像图片路径
  real_name VARCHAR(32),      #用户名，如王小明
  gender TINYINT DEFAULT 1,           #性别  0-女  1-男
  user_level INT DEFAULT 1
);

--电影信息
CREATE TABLE film(
    fid INT PRIMARY KEY AUTO_INCREMENT,
    fname VARCHAR(64) UNIQUE,
    f_ename VARCHAR(64),    #英文名
    type VARCHAR(16),
    area VARCHAR(16),
    icon VARCHAR(16),       #3D,3DMax
    duration INT,
    release_time VARCHAR(18),  #上映时间
    score VARCHAR(8),
    score_people INT,
    total_box_office  INT,  #总票房
    expected_num INT,
    price DECIMAL(4,1),
    details VARCHAR(1024),
    is_onsale TINYINT,
    awards VARCHAR(1024)

);

CREATE TABLE today_office(
    oid INT PRIMARY KEY AUTO_INCREMENT,
    film_name VARCHAR(64) UNIQUE,
    box_office INT
);

-- 首页轮播广告
CREATE TABLE index_carousel(
    cid INT PRIMARY KEY AUTO_INCREMENT,
    img VARCHAR(128),
    href VARCHAR(128)
);

-- 首页商品展示
CREATE TABLE index_movie(
    mid INT PRIMARY KEY AUTO_INCREMENT,
    film_name VARCHAR(64) UNIQUE,
    seq_top_show INT,    #正在热映
    seq_soon_show INT,   #即将上映
    seq_top_sale INT,    #热播
    seq_classic iNT       #经典影片
);


--电影图片
CREATE TABLE film_pic(
    pid INT PRIMARY KEY AUTO_INCREMENT,
    film_name VARCHAR(64),
    sm VARCHAR(1024),
    md VARCHAR(128),
    lg VARCHAR(128),
    aside  VARCHAR(128)
);

--演员
CREATE TABLE film_actor(
    aid INT PRIMARY KEY AUTO_INCREMENT,
    film_id INT,
    star_name VARCHAR(32),
    role VARCHAR(32),  #在影片中的名字
    type VARCHAR(16),  #导演，演员，制片人，编剧，音乐，摄影师，视觉效果，策划，出品人，动作指导，替身，后期导演，剪辑师，艺术指导，美术指导
    is_leading TINYINT,  #0-非主演，1-主演
    pic VARCHAR(128)
);


--演员简介
CREATE TABLE star(
    sid INT PRIMARY KEY AUTO_INCREMENT,
    sname VARCHAR(32),
    s_ename VARCHAR(32),
    job VARCHAR(64),  #导演、演员、编剧。。。。
    birth DATE,
    height FLOAT,
    rank INT,          #影人排名
    fans INT,          #粉丝数
    box_office VARCHAR(32),         #累计票房
    top_works VARCHAR(128),
    works VARCHAR(1024),   #作品
    introduction VARCHAR(1024),  #介绍
    pics VARCHAR(1024),
    awards VARCHAR(1024)

);



--咨询
CREATE TABLE news(
    nid INT PRIMARY KEY AUTO_INCREMENT,
    title VARCHAR(64),
    timer VARCHAR(16),
    view_num INT,
    img VARCHAR(128),
    content VARCHAR(2048),
    com_num INT
);

--评论
CREATE TABLE comment(
    cid INT PRIMARY KEY AUTO_INCREMENT,
    film_id INT DEFAULT 0,
    news_id INT DEFAULT 0,
    user_id INT,
    comment_time DATETIME,
    score FLOAT,
    praise INT DEFAULT 0,
    content VARCHAR(1024)
);

--回复
CREATE TABLE response(
    rid INT PRIMARY KEY AUTO_INCREMENT,
    comment_id INT,
    user_name VARCHAR(32),
    content VARCHAR(256)
);

--影厅座位信息表
CREATE TABLE seat(
    sid INT PRIMARY KEY AUTO_INCREMENT,
    hall INT,
    srow INT,
    scolumn INT
);

--电影场次
CREATE TABLE changci(
    cid INT PRIMARY KEY AUTO_INCREMENT,
    film_id INT,
    ctimer DATETIME,
    hall_id INT
);

--影票售出情况
CREATE TABLE ticket(
    tid INT PRIMARY KEY AUTO_INCREMENT,
    changci_id INT,
    seat_id INT,
    is_sale INT
);

